const color = 'green';

switch(color){
    case 'green':
        console.log('Cross the road');
        break;

    case 'yellow':
        console.log('Wait for a min');
        break;
        
    case 'red':
        console.log('Please, stop here for a min');
        break;

    default: 
        console.log('Default case');    
}