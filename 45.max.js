function maxNumber(a, b, c){
    let result = Math.max(a, b, c);

    return result;
}

let checkMaxNumber = maxNumber(40, 800, 120);

console.log('The max number between 40, 800, 120 is: ' + ' ' + checkMaxNumber);


function minNumber(a, b, c){
    let result = Math.min(a, b, c);

    return result;
}

let checkMinNumber = minNumber(40, 800, 120);

console.log('The min number between 40, 800, 120 is: ' + ' ' + checkMinNumber);