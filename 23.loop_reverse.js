var num = 0;

while(num<=10){
    console.log(num);
    num++;
} // Normal while loop


var num2 = 10;

while(num2 >= 1){
    console.log(num2);
    num2--;
} // Reverse while loop

for(let i = 0; i < 10; i++){
    console.log(i);
} // Normal for loop

for (let a = 20; a >= 1; a--){
    console.log(a);
} // Reverse for loop

